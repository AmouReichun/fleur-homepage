export function checkNgWords(text: string): string[] {
  return NG_WORDS.filter((word) => text.includes(word));
}

// 生成テキストのNG語を安全な表現に自動置換する
export function autoFixNgWords(text: string): string {
  let result = text;
  for (const [ng, safe] of NG_REPLACEMENTS) {
    result = result.split(ng).join(safe);
  }
  return result;
}
